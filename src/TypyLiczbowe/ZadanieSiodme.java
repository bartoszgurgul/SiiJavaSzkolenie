package TypyLiczbowe;

public class ZadanieSiodme {
    public static void main(String[] args){
        short first = 2;
        System.out.println("int >>" + (int) first);

        System.out.println("long  >>" + (long) first);

        System.out.println("Float >>" +(float) first);

        System.out.println("double >>" + (double) first);

        long second = 20;

        System.out.println("int >>" + (int) second);

        System.out.println("byte >>" + (byte) first);

        char third = 'A';

        System.out.println("int >>"+ (int) third);
    }
}
